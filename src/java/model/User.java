
package model;


public class User {

    private String login;
    private String password;
    private String email;
    private String drewno;
    private String zloto;
    private String zelazo;
    private String rycerze;
    private String lucznicy;
    private String machiny;
    private String dniPremium;

    public User() {
    }

    public String getZloto() {
        return zloto;
    }

    public void setZloto(String zloto) {
        this.zloto = zloto;
    }

    public String getZelazo() {
        return zelazo;
    }

    public void setZelazo(String zelazo) {
        this.zelazo = zelazo;
    }

    public String getRycerze() {
        return rycerze;
    }

    public void setRycerze(String rycerze) {
        this.rycerze = rycerze;
    }

    public String getLucznicy() {
        return lucznicy;
    }

    public void setLucznicy(String lucznicy) {
        this.lucznicy = lucznicy;
    }

    public String getMachiny() {
        return machiny;
    }

    public void setMachiny(String machiny) {
        this.machiny = machiny;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDrewno() {
        return drewno;
    }

    public void setDrewno(String drewno) {
        this.drewno = drewno;
    }

    public String getDniPremium() {
        return dniPremium;
    }

    public void setDniPremium(String dniPremium) {
        this.dniPremium = dniPremium;
    }

}
